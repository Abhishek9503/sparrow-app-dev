<script lang="ts">
  import { RequestSection } from "$lib/utils/enums";
  import type { UpdateRequestStateType } from "@workspaces/shared/type";
  import { Label } from "@library/ui";
  export let requestStateSection: string = "";
  export let onUpdateRequestState: UpdateRequestStateType;
  export let authParameterLength = 0;
  export let authHeaderLength = 0;
  export let paramsLength = 0;
  export let headersLength = 0;
  export let autoGeneratedHeadersLength = 0;

  let tabs: {
    name: string;
    id: string;
    count: number;
  }[] = [];

  $: {
    if (
      authParameterLength ||
      authHeaderLength ||
      paramsLength ||
      headersLength ||
      autoGeneratedHeadersLength
    ) {
      tabs = [
        {
          name: "Parameters",
          id: RequestSection.PARAMETERS,
          count: paramsLength + authParameterLength - 1,
        },
        { name: "Body", id: RequestSection.REQUEST_BODY, count: 0 },
        {
          name: "Headers",
          id: RequestSection.HEADERS,
          count:
            headersLength + autoGeneratedHeadersLength + authHeaderLength - 1,
        },
        { name: "Auth", id: RequestSection.AUTHORIZATION, count: 0 },
      ];
    }
  }
</script>

<div class="py-2">
  <!-- Tabs -->
  <div class="d-flex mb-3">
    {#each tabs as tab}
      <button
        class="team-menu__link border-0 me-2 sparrow-fs-12 request-tab me-4 {tab.id ===
        requestStateSection
          ? 'tab-active'
          : ''}"
        role="tab"
        on:click={() => {
          onUpdateRequestState({ requestNavigation: tab.id });
        }}
      >
        <span class=""
          >{tab.name}
          {#if tab.count}
            <Label number={tab.count} />
          {/if}
        </span>
      </button>
    {/each}
  </div>
</div>

<style>
  .team-menu__link {
    color: #8a9299;
    background-color: transparent;
    border-bottom: 2px transparent;
  }
  .tab-active {
    color: white;
    border-bottom: 2px solid var(--send-button) !important;
  }
</style>
