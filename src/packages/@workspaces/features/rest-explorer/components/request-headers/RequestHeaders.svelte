<script lang="ts">
  import type { KeyValuePair } from "$lib/utils/interfaces/request.interface";
  import { TabularInput } from "@workspaces/common/components";
  import { createDeepCopy } from "$lib/utils/helpers/conversion.helper";
  import AngleUp from "$lib/assets/angle-up.svelte";
  import AngleDown from "$lib/assets/angle-down2.svelte";

  export let environmentVariables = [];
  export let onHeadersChange;
  export let onAutoGeneratedHeadersChange;
  export let headers;
  export let autoGeneratedHeaders;
  export let authHeader;
  export let onUpdateEnvironment;

  let showGeneratedHeader: boolean = false;

  const handleHeaderChange = (pairs: KeyValuePair[]): void => {
    onHeadersChange(pairs);
  };

  const handleAutoGeneratedChange = (pairs: KeyValuePair[]): void => {
    onAutoGeneratedHeadersChange(pairs);
  };
</script>

<section
  class="w-100 ps-1"
  style="height: calc(100% - 45px); overflow-y: scroll;"
>
  <TabularInput
    keyValue={createDeepCopy(headers)}
    callback={handleHeaderChange}
    {environmentVariables}
    {onUpdateEnvironment}
  />
  <div>
    <div class="d-flex align-items-center">
      <span class="text-labelColor generated-para me-2">
        Show auto-generated headers
      </span>
      <div class="form-check form-switch">
        <input
          class="form-check-input"
          type="checkbox"
          role="switch"
          id="flexSwitchCheckDefault"
          checked={showGeneratedHeader}
          on:change={() => {
            showGeneratedHeader = !showGeneratedHeader;
          }}
        />
      </div>
    </div>
  </div>

  {#if showGeneratedHeader}
    <TabularInput
      mode={"READ"}
      readable={authHeader}
      keyValue={createDeepCopy(autoGeneratedHeaders)}
      callback={handleAutoGeneratedChange}
      {environmentVariables}
      {onUpdateEnvironment}
    />
  {/if}
</section>

<style>
  .generated-para {
    font-size: 12px;
  }
</style>
