import { TabRepository } from "$lib/repositories/tab.repository";

export class HeadersViewModel {
  private tabRepository = new TabRepository();
  constructor() {}
  public updateHeaders = async (data: any, route: string) => {
    await this.tabRepository.setRequestProperty(data, route);
    return;
  };
  public updateAutoGeneratedHeaders = async (data: any, route: string) => {
    await this.tabRepository.setRequestProperty(data, route);
    return;
  };
}
